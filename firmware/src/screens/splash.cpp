// =============================================================================
// Helper — draw one full local bitmap and push to display
// =============================================================================
static void _drawLocalBitmap(
    Adafruit_SH1106G *disp,
    const unsigned char *bmp,
    int16_t x,
    int16_t y,
    int16_t w)
{
  disp->clearDisplay();
  disp->drawBitmap(x, y, bmp, w, LOGO_H, SH110X_WHITE);
  disp->display();
}

// =============================================================================
// runSplash — drives all four screens simultaneously
//
// Screen 0: 118x42 bitmap at x = 10
// Screen 1: 128x42 bitmap at x = 0
// Screen 2: 128x42 bitmap at x = 0
// Screen 3: 118x42 bitmap at x = 0
//
// Animation: y scrolls from -LOGO_H to 64.
// =============================================================================
void runSplash(
    void (*selectFn)(uint8_t ch),
    uint8_t ch0, Adafruit_SH1106G *disp0,
    uint8_t ch1, Adafruit_SH1106G *disp1,
    uint8_t ch2, Adafruit_SH1106G *disp2,
    uint8_t ch3, Adafruit_SH1106G *disp3)
{
  static const int16_t X_POS[4] = {10, 0, 0, 0};

  for (int16_t y = -LOGO_H; y <= 64; y += 3)
  {
    selectFn(ch0);
    _drawLocalBitmap(disp0, _pajero1, X_POS[0], y, LOGO1_W);
    selectFn(ch1);
    _drawLocalBitmap(disp1, _pajero2, X_POS[1], y, LOGO2_W);
    selectFn(ch2);
    _drawLocalBitmap(disp2, _pajero3, X_POS[2], y, LOGO3_W);
    selectFn(ch3);
    _drawLocalBitmap(disp3, _pajero4, X_POS[3], y, LOGO4_W);
  }
}